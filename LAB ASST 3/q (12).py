r_or = ip1 or ip2
r_not1 = not ip1
r_not2 = not ip2

print(f"Answer for AND: {r_and}")
print(f"Answer for OR: {r_or}")
print(f"Answer for NOT (first value): {r_not1}")
print(f"Answer for NOT (second value): {r_not2}")
